export function translateZiwei(text: string | undefined): string {
  if (!text) return "";
  
  // Check exact match first
  if (hanjaToKoreanMap[text]) {
    return hanjaToKoreanMap[text];
  }
  
  // Replace characters
  let translated = text;
  for (const [hanja, korean] of Object.entries(hanjaToKoreanMap)) {
    translated = translated.replace(new RegExp(hanja, 'g'), korean);
  }
  
  return translated;
}
